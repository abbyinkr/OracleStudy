--○ TBL_SAWON 테이블 백업 (2021-04-07 10:00:00)
CREATE TABLE TBL_SAWONBACKUP
AS
SELECT *
FROM TBL_SAWON;
--==>> Table TBL_SAWONBACKUP이(가) 생성되었습니다.
--> TBL_SAWON 테이블의 데이터들만 백업을 수행 
--  즉, 다른 이름의 테이블 형태로 저장해 둔 상황

--○ 확인
SELECT *
FROM TBL_SAWONBACKUP;
SELECT *
FROM TBL_SAWON;

UPDATE TBL_SAWON
SET SANAME = '이무림';

COMMIT;

-- 위와 같이 UPDATE 처리 이후에 COMMIT 을 수행하였기 때문에
-- ROLLBACK 은 불가능한 상황이다.
-- 하지만, TBL_SAWONBACKUP 테이블에 데이터를 백업해 두었다.
-- SANAME 컬럼의 내용만 추출하여 '이무림' 대신 넣어줄 수 있다는 것이다.

UPDATE TBL_SAWON
SET SANAME = '김가영'
WHERE SANO = 1001;

UPDATE TBL_SAWON
SET SANAME = '김서현'
WHERE SANO = 1002;
--> 이거 못해 너무 많아

UPDATE TBL_SAWON
SET SANAME = TBL_SAWONBACKUP 테이블의 1001번 사원의 사원명;

UPDATE TBL_SAWON
SET SANAME = (SELECT SANAME
              FROM TBL_SAWONBACKUP 
              WHERE SANO = TBL_SAWON.SANO);
--==>> 16개 행 이(가) 업데이트되었습니다.

SELECT *
FROM TBL_SAWONBACKUP;

SELECT *
FROM TBL_SAWON;

COMMIT;
--==>> 커밋 완료.
